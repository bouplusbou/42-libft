#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	l;

	l = ft_strlen(s1);
	i = 0;
	while (s2[i] && i < n)
		s1[l++] = s2[i++];
	s1[l] = '\0';
	return (s1);
}
